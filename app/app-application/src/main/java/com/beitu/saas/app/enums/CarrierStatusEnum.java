package com.beitu.saas.app.enums;

import com.fqgj.common.api.enums.MsgCodeEnum;

/**
 * @author linanjun
 * @create 2018/4/11 下午5:52
 * @description
 */
public enum CarrierStatusEnum implements MsgCodeEnum {

    UNAUTHORIZED(1, "未认证"),
    SUCCESS(2, "认证成功"),
    FAILURE(3, "认证失败");

    CarrierStatusEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    private Integer code;

    private String msg;

    @Override
    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    @Override
    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

}
