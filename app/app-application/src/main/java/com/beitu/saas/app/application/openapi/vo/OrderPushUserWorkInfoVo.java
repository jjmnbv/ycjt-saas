package com.beitu.saas.app.application.openapi.vo;

public class OrderPushUserWorkInfoVo {
    
    private String career;
    
    private String salary;
    
    private Integer payDay;
    
    private String companyName;
    
    private String companyDetailAddress;
    
    public String getCareer() {
        return career;
    }
    
    public void setCareer(String career) {
        this.career = career;
    }
    
    public String getSalary() {
        return salary;
    }
    
    public void setSalary(String salary) {
        this.salary = salary;
    }
    
    public Integer getPayDay() {
        return payDay;
    }
    
    public void setPayDay(Integer payDay) {
        this.payDay = payDay;
    }
    
    public String getCompanyName() {
        return companyName;
    }
    
    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }
    
    public String getCompanyDetailAddress() {
        return companyDetailAddress;
    }
    
    public void setCompanyDetailAddress(String companyDetailAddress) {
        this.companyDetailAddress = companyDetailAddress;
    }
}
